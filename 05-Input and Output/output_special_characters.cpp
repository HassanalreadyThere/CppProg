#include <iostream>
using namespace std;

int main() {
    cout << "Demonstrating Special Characters in C++\n";
    cout << "---------------------------------------\n";

    cout << "New line example:\nHello\nWorld\n\n";
    cout << "Tab example:\tC++\tProgramming\n\n";
    cout << "Backslash: \\\n";
    cout << "Double quote: \"Hello\"\n";
    cout << "Single quote: \'A\'\n";
    cout << "Carriage return example:\rOverwritten line\n";
    cout << "Backspace: ABC\bD\n";
    cout << "Form feed: First page\fSecond page\n";
    cout << "Alert (beep): \a\n";

    return 0;
}
